 //Función de retardo en segundos (función aproximada)
void delay_s(uint32_t segundos) {
	uint32_t i, conversion;
	conversion = segundos*3700000;
	for (i = 0; i < conversion; i++) {
		__asm volatile ("nop");
	}
}

//Función de retardo en milisegundos (función aproximada)
void delay_ms(uint32_t milis) {
	uint32_t i, conversion;
	conversion = milis*3700;
	for (i = 0; i < conversion; i++) {
		__asm volatile ("nop");
	}
}
